export class SimulatorInfo {

    /**
     * constructor - To calculate the price of a car
     *
     * @param  {string} name     description
     * @param  {string} carName  description
     * @param  {number} carValue description
     * @param  {number} price    description
     */
    constructor(name, carName, carValue, price) {
        this.name = name;
        this.carName= carName;
        this.carValue= carValue;
        this.price= price;
    }
}
